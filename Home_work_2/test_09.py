# 1 создаем список

list_1 = [42, 69, 322, 13, 0, 99, -5, 9, 8, 7, -6, 5]

# 2 создаем цикл while, бесконечный цикл, пока не выполнится условие для закрытия программы

while True:      #  классика бесконечного цикла, (True - это правда или истина)
    print(list_1)  # так мы запустим бесконечный вывод списка в терминал
    break

